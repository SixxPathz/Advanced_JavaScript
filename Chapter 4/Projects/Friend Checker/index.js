let friendName = prompt("What is your friend's name?").toLowerCase();

switch (friendName) {
  case "john":
    alert("John is a friend");
    break;
  case "pulane":
    alert("Pulane is a friend!");
    break;
  case "xolani":
    alert("Xolani is a friend");
  case "sandile":
    alert("Sandile is a friend");
    break;
  default:
    alert("That is a stranger!");
    break;
}
